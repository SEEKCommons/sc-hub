dans les jours à venir&nbsp;: s’il faut voter en faveur ou non de la
permission de la chasse au loup dans l’Etat du Michigan. En tant que
Président de la <em>Commission des Ressources naturelles</em> (CRN ou NRC en
anglais), un organe consultatif composé de sept cadres du <em>Ministère
d’Etat des Ressources Naturelles</em> (MRN ou DNR en anglais), Richardson
avait traité sa juste part des questions litigieuses pendant son mandat au
CRN depuis 2007. Mais il pouvait se souvenir qu’aucun problème n’avait
suscité une telle passion de tous les côtés comme celui de la chasse au loup.
En conséquence, il a ressenti beaucoup de pressions pour conduire le CRN à
prendre la bonne décision, c’est-à-dire celle basée sur des principes
scientifiques, le respect <a data-edgenote="a-heated-debate">des besoins des
personnes qui sont affectées</a> par la présence de loups et la sensibilité à
l’opinion publique.</p>
<p>La CRN, dont le travail consiste à réguler les espèces chassables, ne
semble pas être l’organe décisionnel le plus approprié quand il s’agit des
loups dans le Michigan. En effet, compte tenu de l’extermination systématique
des espèces aux États-Unis au cours des 19ème et début du 20ème siècle et son
inscription ultérieure comme une espèce en voie de disparition en 1974,
l’idée de chasser le loup était inimaginable pendant des décennies.
Toutefois, le retour miraculeux du loup a conduit à sa radiation de la liste
en janvier 2012, un geste qui a entraîné le retour du contrôle des espèces
chassables par les Etats, notamment dans le Michigan, où réside maintenant
une population croissante de loups. En décembre 2012, le Gouverneur de l’Etat
du Michigan Rick Snyder et le Parlement de l’Etat du Michigan ont
officiellement désigné le loup comme du gibier (espèce chassable), ce qui a
entraîné que la responsabilité de réglementer toute chasse publique au loup
revienne au CRN.</p>
<section>
  <p>Pour avoir résidé toute sa vie dans la Péninsule Supérieure (PS ou UP)
  de l’Etat du Michigan, où se trouve la population de loups de l’Etat, mais
  aussi en sa qualité de passionné du plein air, d’homme d’affaires et
  ingénieur possédant une vaste expérience en matière de ressources
  naturelles, Richardson avait des sentiments personnels et professionnels
  contradictoires sur cette affaire de loup. D’une part, il a vu <a data-edgenote="residents-weigh-hunt">le retour du loup comme un signe
  de l’assainissement et de la prospérité de l’environnement naturel</a>, et
  il veut protéger l’équilibre écologique. D’autre part, il connaissait
  personnellement <a data-edgenote="residents-weigh-hunt">certaines
  personnes dont le bétail avait été la proie des loups</a> et était
  directement informé sur les conflits hommes-loups issus de cette question
  politiquement importante. Les préoccupations scientifiques, politiques et
  communautaires autour de la possibilité d’une chasse au loup laissaient
  transparaître que quelle que soit la décision qu’allait prendre le CRN,
  tout le monde ne devait pas être satisfait. Et Richardson devrait être la
  personne du CRN qui devrait prendre cette décision en tenant compte des
  pressions du Gouverneur, du Parlement, des défenseurs du Loup, des groupes
  tribaux, des chasseurs, des éleveurs et du public, qui critiqueront
  fortement cette décision.</p>
  <p>Maintenant, à la veille de cette importante décision, Richardson
  réfléchissait sur ses options. Devait-il prendre en compte les appels des
  personnes opposées à la chasse et de certains scientifiques qui pensent que
  les loups devraient continuer à être entièrement protégés? Devait-il
  permettre à la chasse publique de continuer, ou encourager le DNR à trouver
  d’autres moyens pour contrôler la population des loups&nbsp;? Comment les
  conflits entre les loups et les humains pourraient être gérés plus
  efficacement&nbsp;? Quelles sont les conséquences des différentes décisions
  pour les parties prenantes&nbsp;? Richardson retourna à sa lecture avec ces
  questions qui pesaient lourdement dans son esprit.</p>
</section>

<h1>L’histoire de loups gris dans les États-Unis et le Michigan</h1>
<section>
  <p>L’histoire des <a data-edgenote="gray-wolf">loups
  gris</a> dans le Michigan, qui évolue en parallèle avec la grande histoire
  des espèces dans les Grands Lacs et dans toute l’Amérique du Nord, est
  faite de procès et de triomphes. Originaires du Michigan, les loups
  parcouraient chaque région de l’État, en coexistant avec les humains.</p>
  <p>Dans les années 1800, le peuplement de l’ouest des États-Unis par
  l’Homme a intensifié les conflits hommes-loups qui étaient déjà déclenchés
  par l’histoire du loup gris. Les animaux qui avaient été les proies des
  loups – les bisons, les wapitis et les orignaux (élans) – étaient tués par
  les nouveaux colons pour se nourrir ou pour le sport. Comme leur source de
  nourriture naturelle a diminué, les loups se sont tournés vers les moutons
  et le bétail des colons pour se nourrir et ont involontairement déclenché
  une campagne d’extermination qui dura cent ans. Les éleveurs et les
  professionnels «&nbsp;tueurs de loups&nbsp;» tuaient aveuglément ces
  animaux à l’aide de poison, de pièges et autres méthodes afin de protéger
  leurs troupeaux. L’extermination prit de l’ampleur à tel point que le
  Gouvernement fédéral promulgua un programme d’éradication dans tout le
  pays, offrant des primes pour les peaux d’animaux morts. Même le président
  Théodore Roosevelt, autrement connu comme un champion pour la préservation
  de l’environnement, a reflété le sentiment d’une Nation lorsqu’il appela
  les loups “bêtes de déchets et de désolation.”</p>
</section>
<section>
  <p>Le programme fédéral de primes de fourrure de loup a décimé les
  populations de loups à travers tout le pays, et les loups du Michigan n’ont
  pas été épargnés. Les loups avaient été éliminés de la partie méridionale
  de l’État dans les années 1830, et dans les années 1900, ils avaient
  pratiquement disparu. Une petite population qui était restée dans l’île
  Royale de l’Isle au nord de la <a data-edgenote="michigan">Péninsule Supérieure du
  Michigan</a>, était considérée probablement comme une des dernières
  populations des loups en Amérique du Nord pendant <a
data-edgenote="gray-wolf-range">l’étape finale d’extermination du
  loup dans les années 1960</a>.</p>
  <p>Dans un revirement de situation majeur pour les espèces, l’Etat du
  Michigan avait fait comme dans les autres États, en légiférant la
  protection des loups en 1965. Cela a fait que le peu de loups restants —
  estimé à six dans la Péninsule Supérieure et une autre population minuscule
  sur l’île Royale de l’Isle — a bénéficié d’une grâce ou sursis avec l’arrêt
  systématique de la chasse. Ce qui leur a permis de se reproduire lentement.
  Leur reproduction a été renforcée par l’inscription de 1974 qui considère
  les loups comme une espèce en voie de disparition, peu après la mise en
  place de l’association fédérale des <em>Actes pour les espèces menacées
  d’extinction</em> (AEME) en 1973.</p>
</section>
<p>Il y avait des signes que le retour des loups était probable comme le
soulignent les rapports d’observation dans la PS du Michigan, mentionnant
qu’ils avaient commencé à augmenter dans les années 1970. Pour essayer
d’accélérer cette reprise, les biologistes avaient tenté d’introduire en 1974
quatre loups venus de la PS du Minnesota. Le plan échoua lorsque les humains
tuèrent tous les quatre loups avant même qu’ils eussent le temps de se
reproduire, conduisant ainsi le DNR du Michigan à décider de ne plus
intervenir à propos de la reproduction des loups dans l’Etat. Cette
régénération naturelle débuta quand un couple de loups avait été découvert en
provenance des États voisins de la PS centrale dans années 1980. Comme le
couple a commencé à se reproduire dans les années 1990, <a
data-edgenote="mi-wolf-population">une augmentation constante de la
population de loups a suivi</a>. En 2010, 687 loups ont été recensés comme
vivant dans l’État. Simultanément, les gens devinrent de plus en plus
favorables au retour des loups, notamment la majorité des habitants du
Michigan ont soutenu le rétablissement du loup dans l’État.</p>
<p>La protection fédérale et étatique a été déterminante dans le
rétablissement des loups. Le Plan de relance des loups, mis au place en 1994
pour répondre aux exigences de l’AEME, a stipulé que la population de loups
du Wisconsin – Michigan doit dépasser 100 pendant au moins cinq années
consécutives pour assurer la survie de l’espèce. En 1997, <a
data-edgenote="perversion-of-democracy">l’Etat du Michigan a complété son
propre plan de relance</a>, le redressement des loups du Michigan et le plan
de gestion, qui recommande une population de 200 pendant cinq années
consécutives. Atteindre ces objectifs devrait ouvrir la voie pour
l’élimination des espèces étatiques et fédérales de la liste des espèces
menacées d’extinction. En effet, le Parlement du Michigan et les services
américains de la Pêche et de la Faune et la Flore, ont supprimé les loups de
l’Etat du Michigan des listes fédérales des espèces en voie de disparition,
respectivement en 2009 et 2012. La radiation de la liste fédérale a donné le
contrôle total à l’état du Michigan pour gérer ses populations de loup.</p>

<h1>La biologie, la conservation et la gestion scientifique</h1>
<h2>L’importance des loups dans un écosystème</h2>
<section>
  <p>Les loups gris sont considérés comme une espèce clé qui joue un rôle
  unique dans le maintien de la santé de nombreux écosystèmes. L’étude
  biologique du Parc National de Yellowstone et d’autres zones qui ont été
  repeuplés par des loups met en lumière la cascade des incidences que leur
  retrait et leur retour ont eues sur l’écosystème. Pour commencer, les loups
  aident à réguler la taille et la santé des populations des proies en
  sélectionnant les animaux jeunes ou infirmes à attaquer. En même temps,
  cela aide à gérer le nombre de proies qui atteint sa maturité reproductive
  et supprime les animaux malades de la harde, en régulant ainsi la
  croissance démographique et en réduisant la transmission de la maladie.</p>
  <p>En outre, la présence des loups dans le paysage modifie les habitudes de
  déplacement des proies au profit de la végétation. Cerfs, bisons, wapitis
  et autres espèces nomades ont tendance à se nourrir en plein air sur les
  gaules et nouvelle croissance lorsque les prédateurs sont absents, ce qui
  réduit la quantité de végétation qui pousse dans une zone. La présence des
  loups oblige ces animaux à chercher une couverture et à se déplacer dans
  des zones moins sensibles. Cela permet à la végétation de croître et à des
  régions riveraines de prospérer. L’effet de cascade continue étant donné
  que la végétation plus lourde attire des castors, des petits rongeurs, des
  oiseaux et autres espèces dont la présence crée un habitat pour les
  poissons et améliore la santé des cours d’eau. Enfin, les carcasses
  laissées par les loups servent de nourriture aux charognards, qui
  contribuent à la biodiversité du paysage.</p>
</section>
<p>Bien que la réintroduction du loup a conduit à une spectaculaire
restauration de la biodiversité et les écosystèmes riverains à Yellowstone,
les chercheurs se sont rendus compte que la <a
data-edgenote="death-of-832f">réintroduction d’un prédateur après
une absence de 70 ans</a> ne résout pas tous les problèmes. Dans certaines
régions, les saules et les peupliers ne parviennent pas toujours à s’épanouir
et les castors ne sont pas de retour. C’est parce que la migration lourde
(massive), en l’absence des loups, a réellement changé la forme et le débit
des rivières. L’habitat est devenu impropre aux saules et aux castors, tandis
que les autres secteurs sont devenus plus accessibles aux bisons, un
herbivore qui n’est pas affecté (menacé) par les loups. Même si les loups
introduits ont maintenant réduit les déplacements du wapiti, il faudra plus
pour inverser l’hydrologie et changer le paysage de Yellowstone.</p>
<p>Dans le Michigan, les loups sont un prédateur essentiel du cerf de
Virginie, une espèce de migratoire (navigation) qu’on trouve couramment dans
les États-Unis. En partie à cause d’un manque de prédateurs naturels, les
densités de cerfs sont considérablement plus élevées aujourd’hui que les
estimations historiques et un nombre surabondant de cerfs peut avoir un
impact dévastateur sur l’écologie forestière et la diversité des plantes
indigènes. En tant que chercheur du Service des forêts Stephen Horsley a fait
remarquer: la population âgée de cerfs aspire “les semis d’espèces hautement
recommandées, réduisant la diversité des plantes à l’extrême, et en créant
quasiment des mono-cultures.”</p>
<section>
  <p>Les chasseurs ont noté que la population croissante de loups dans la
  Péninsule Supérieure du Michigan coïncide avec <a
  data-edgenote="bear-wolf-coyote-deer-pop">une population de cerfs en
  déclin</a>. Toutefois, des études récentes sur les causes de mortalité de
  cerfs dans ce domaine mettent plutôt en lumière des hivers durs et les
  coyotes comme les principaux responsables.</p>
  <p>Maintenir l’avantage écologique des loups est un objectif explicit
  <em>du Plan de gestion des loups du DNR du Michigan</em>, qui reconnaît que
  les loups peuvent contrôler et améliorer la santé des proies, et ont des
  effets indirects positifs sur les communautés végétales. Bien que 200 loups
  correspondent à la population minimale viable des loups, il est reconnu que
  cette taille n’est peut-être pas suffisante pour fournir ces prestations
  écologiques. Robert Beschta, qui a étudié le système de Yellowstone pendant
  plus d’une décennie, affirme que pour la restauration écologique, “il
  serait peut être nécessaire que les loups soient non seulement présents,
  mais qu’ils assurent également une densité écologiquement efficace.”</p>
</section>
<h2>La responsabilité de la gestion du loup dans le Michigan&nbsp;: le DNR et
les autres entités</h2>
<section>
  <p>Le ministère des ressources naturelles du Michigan est responsable de la
  conservation, la protection et la gestion des ressources naturelles et
  culturelles de l’Etat. La DNR vise à équilibrer les écosystèmes
  florissants, les économies tributaires des ressources naturelles saines,
  les loisirs durables et la jouissance de ces ressources.</p>
  <p>Une fois que les loups ont été radiés de la liste fédérale des espèces
  menacées en 2012, la gestion du loup a été ajoutée à la liste des
  responsabilités du MRN. Car il régule la faune et les espèces chassables ou
  gibiers, la DNR conçoit la gestion du loup tant du point de vue des loups
  comme espèce sauvage et comme gibier. En conséquence, la prise de décisions
  sur la gestion de la population de loups du Michigan prend en compte les
  considérations biologiques, sociales et économiques, comme nous le verrons
  plus loin ci-dessous.</p>
</section>
<p>Divers comités et groupes au sein de la DNR contribuent à la prise de
décision de gestion de la faune dans l’État. <em>Le Conseil consultatif de la
gestion des loups du Michigan</em> (CCGF) a été formé pour examiner
périodiquement le Plan de gestion du Loup et pour fournir des orientations
pour la DNR sur quels changements sont nécessaires. WMAC est représenté par
les gens d’organismes et de groupes d’intervenants. À un niveau supérieur, le
CRN, un groupe de sept membres nommés par le gouverneur, est chargé d’aider à
élaborer des politiques ayant une influence sur les priorités et les
orientations de la DNR. Le CRN a aussi le pouvoir de réglementer les espèces
chassables ou gibiers.</p>
<h2>La gestion du loup</h2>
<p>L’État et les organismes fédéraux de réglementation étudient les moyens
d’atteindre les objectifs sociaux et écologiques dans le processus
décisionnel de gestion du loup. Toutefois, <em>le Plan de récupération et de
gestion du loup par le Michigan</em> en 1997 a été créé en grande partie dans
le cadre de la protection de l’espèce et pour assurer la réussite de sa
récupération, la version 2008 du plan a été mise à jour pour avoir <a
data-edgenote="recovery-management-plan-goals">une vision plus globale de la
protection des loups</a> tout en tenant compte de leur coexistence avec les
humains.</p>
<p>Définir une politique de gestion réussie du loup dépend des objectifs de
gestion de la population et le cadre pour l’établissement de ces objectifs.
La DNR fait remarquer que la <strong>capacité biologique&nbsp;</strong>et la
<strong><a data-edgenote="measure-tolerance-carrying-capacity">capacité
sociale</a></strong>&nbsp;de loups devraient être considérées. La capacité
biologique se réfère à la notion que la population d’une espèce est limitée
par la capacité de l’environnement à la supporter en termes de nourriture,
d’eau et d’habitat. La capacité sociale renvoie à la tolérance humaine pour
une espèce à limiter sa population. Une gestion réussie des loups, signifie
donc équilibrer la viabilité biologique de l’espèce et son rôle important
dans l’écosystème, avec la tolérance des humains qui vivent dans sa présence.
Comme biologiste de la faune, David Hammill a soutenu devant le CRN que la
viabilité de la population de loups dans l’Etat dépendait de la capacité des
êtres humains et des loups de coexister pacifiquement. Ainsi, là où les
espèces sont en particulier des conflits avec les humains subsistent, la
gestion des conflits est essentielle à la bonne prise de décision
scientifique. Les objectifs de gestion sont développés de façon à établir un
équilibre entre avantages de l’écosystème et les conflits humains.</p>
<section>
  <p>La <em>table ronde des loups du Michigan</em>, un Comité d’intervenants
  reflétant les différents points de vue, a été convoquée par le DNR pour
  fournir des recommandations sur les questions de loup. Le rapport de 2006
  de la Commission a indiqué que «&nbsp;les objectifs de gestion du loup
  devraient être fondées sur l’impact des loups plutôt que l’abondance ou le
  nombre de loups qui seraient le contre poids des préoccupations écologiques
  et sociales. Les 20 membres du Comité ont en leur sein des représentants de
  groupes aussi divergents que le Bureau des <em>fermiers du Michigan</em>,
  l’<em>Alliance des sportifs de la Péninsule Supérieure</em>, la <em>Société
  protectrice des animaux du Michigan</em>, et l’<em>Alliance des loups des
  bois</em>. Les membres étaient parvenus à un consensus sur la nécessité
  d’avoir un contrôle létal des loups comme une “option pour répondre aux
  conflits impliquant des loups et du bétail. Toutefois, le plan loup révisé
  devrait accorder une grande priorité pour développer, évaluer et mettre en
  application des méthodes de gestion non létale.”</p>
  <p>Alors que le groupe pourrait s’entendre sur beaucoup de choses, ils
  pourraient s’entendent sur le fait qu’une saison de chasse/piégeage soit
  appropriée ou non. “Après une délibération importante, nous avons conclu
  qu’un consensus sur les principes directeurs concernant cette question
  n’était pas possible parce que le désaccord portait principalement sur des
  différences importantes dans les valeurs fondamentales”.</p>
</section>
<p>La gestion de la population des loups peut impliquer diverses méthodes de
contrôle de la population, y compris le contrôle de la fertilité, la
translocation vers des zones moins peuplées et l’abattage sélectif. La
«&nbsp;Gestion passive&nbsp;» qui permet essentiellement à la population
d’atteigne sa capacité de charge biologique, est aussi une option, bien
qu’elle n’aborde pas la question de la capacité de charge sociale. En règle
générale, aujourd’hui ces méthodes sont réalisées par des professionnels tels
que ceux du Service américain de la pêche et la vie sauvage, bien que dans le
passé, le gouvernement ait autorisé et même payé des personnes pour tuer des
loups. La désignation des loups comme gibier qui peut être chassé de façon
récréative a crée une autre option pour la gestion de la population de loups
avec des avantages potentiels. Le niveau de prise de décision est déterminé
par les scientifiques et les gestionnaires de façon à assurer la viabilité.
Les chasseurs bénéficient d’une nouvelle opportunité, et les résidents
perçoivent que le problème du conflit homme-loup est traité.</p>
<p>Les scientifiques des deux côtés du débat semblent reconnaître
l’importance de l’équilibre entre les préoccupations sociales et écologiques,
mais le désaccord sur le comment de ces préoccupations doit être pesé contre
l’autre. Certains groupes de défenseur de loup, dont les membres comprennent
les scientifiques et les onscientists, remmettent en question cette approche
de gestion scientifique, en affirmant qu’il n’y a aucun besoin biologique ou
un solide appui scientifique à une chasse publique au loup. En outre, ils
font valoir que les préoccupations du public concernant les interactions
homme-loup sont exagérées et que ces interactions tendent à être tellement
rares qu’une chasse publique au loup n’est pas une approche raisonnable pour
y remédier.</p>

<h1>Les humains et les loups : une relation délicate</h1>
<p>Les loups occupent une place distincte dans la culture occidentale
européenne. Les histoires d’enfants et les contes de fées écrits il y a des
siècles offrent un aperçu d’une attitude face à l’animal: <em>le garçon qui
criait au loup</em>, <em>Little Red Riding Hood</em>, et <em>les trois petits
cochons</em> présentent tous l’archétype du grand méchant loup. Mais au XXIe
siècle avec une population urbaine plus développé, un paysage plus développé
et un très petit nombre de prédateurs, le risque d’attaque par des loups
est-il un réel problème&nbsp;? Les loups ont-ils besoin d’être traités comme
une menace à la vie humaine et à nos moyens de subsistance&nbsp;?</p>
<h2>Les attaques sur les personnes et les chiens</h2>
<p>Il y a quelques documents sur des décès humains causés par le loup en
Amérique du Nord. Depuis 2000, trois décès documentés sont survenus à la
suite des attaques de loup (deux en Alaska) et un en Ontario. Un rapport de
2002 réalisée par <em>l’Institut norvégien de recherche sur la Nature</em> a
cité au total 18 attaques de loup en Amérique du Nord au XXe siècle. Le
rapport identifie quatre facteurs associés aux attaques sur les
humains&nbsp;: la rage, l’accoutumance (quand les loups perdent la peur de
l’homme), la provocation et les milieux fortement modifiés. En 2002
également, le <em>Département de la pêche et la chasse de l’Alaska</em> a
déclaré qu’aucun décès humain n’a été attribué à des loups sains depuis 1900,
par opposition aux décès attribués aux loups atteints de la rage.</p>
<p>Alors qu’aucune attaque documentée de loup sur les êtres humains n’a eu
lieu dans le Michigan, il y a eu une poignée d’attaques sur les chiens. Un
article de 2011 a trouvé 34 cas de déprédations de chiens domestiques entre
1996 et 2008. Par ailleurs, une majorité des <a
data-edgenote="too-close-for-comfort">résidants dans la Péninsule
Supérieure. pense que les loups sont de plus en plus à l’aise avec les
humains</a>, ce qui accroît les risques que loups attaquent les gens.</p>
<section>
  <p>Dans un mémo d’avril 2013, le DNR a fait un commentaire sur les
  interactions homme-loup dans le comté de Gogebic, MI:</p>
  <blockquote>
    <p>Depuis 2010, nous avons enregistré 91 plaintes du comportement des
    loups nuisibles se déplaçant dans les limites de la ville, des loups
    chassant des chiens dans les zones résidentielles et les loups se
    déplaçant à proximité d’enfants en attente dans des abris d’autobus
    scolaires ou dans des garderies. Les Services de la faune et la flore de
    USDA, sous notre direction, a passé énormément de temps à répondre à ces
    plaintes. Depuis 2010, ils ont utilisé des techniques de harcèlement
    (coquilles de pirate) et fourni des conseils techniques aux résidents
    pour éviter des interactions négatives entre le loup et l’homme. Lorsque
    les techniques non létales ont échoué à résoudre les problèmes, les
    Services de la faune a retiré 17 loups de la zone.</p>
  </blockquote>
  <p>Les données du siècle dernier indiquent que les attaques de loups sur
  les personnes et les animaux de compagnie en Amérique du nord sont rares,
  mais le passé ne peut ne pas être indicatif de l’avenir, surtout si l’on
  tient compte de l’absence des loups pendant la majeure partie du XXe
  siècle.</p>
</section>
<h2>Les perturbations sur l’élevage</h2>
<p>Le type de perturbation de l’élevage le plus visible est la déprédation,
quand les loups attaquent et blessent ou tuent le bétail. En Amérique du
Nord, les ovins et bovins sont les principales cibles, mais n’importe quel
type de bétail peut faire l’objet de déprédation. Entre 1996 et 2008, 87
déprédations (ravages) de bétail ont été enregistrées dans le Michigan,
<a data-edgenote="most-polarizing-farmer">avec 70&nbsp;% des
événements impliquant des bovins</a>. Il existe une relation positive (ou
fondée) entre le nombre élevé de loups et le ravage des bovins dans la
Péninsule Supérieure. Et les données de 1996 – 2008 suggèrent que pour chaque
100 autres loups ajoutés, il faudra compter <a
data-edgenote="cattle-depredations-mi-radio">trois moments de déprédation
supplémentaires de bovins</a>. Même quand les incursions des loups vers les
fermes ou ranchs ne résultent pas directement par des blessures, mais le
simple fait pour les bovins de les voir, les effraie et cela peut les amener
à être moins enclins (disposés) à faire paître. La réduction du pâturage a
pour résultats de ne pas favoriser la croissance et la reproduction des
bovins. La simple présence observée des loups peut aussi entraver (ou gêner)
la l’action des chiens dans l’élevage du bétail, et cela a une incidence
négative sur l’efficacité les opérations (ou l’exploitation) de l’élevage.
Les agriculteurs et les éleveurs notent tout de même (ou regrettent) que des
impacts indirects sur la productivité ne sont pas indemnisés en vertu des
programmes visant à atténuer l’impact des déprédations des loups.</p>

<h1>Une saga politique et réglementaire</h1>
<p>Le sentier qui mène jusqu’à la décision du CRN est marqué par un drame
politique, en ce sens que la législature ou le Parlement du Michigan a pris,
à plusieurs reprises, des mesures pour faire avancer l’État vers une chasse
au loup. Et des groupes de chasseurs ont essayé d’utiliser des mesures comme
le vote pour promouvoir une chasse. Les groupes anti-chasse, quant à eux,
cherchèrent à utiliser une telle mesure pour arrêter la chasse. Ainsi, la loi
<em>377 des actes publics</em> avaient investi de pouvoir exclusif le NRC
pour réguler la capture ou la chasse de gibier dans le Michigan, et retirant
à cet effet ce pouvoir au directeur du <em>département (ministère) des
ressources naturelles</em>. La proposition de vote par bulletin n’était plus
intéressante. C’était une initiative appuyée par les <a
data-edgenote="hunting-conservation-economics">organisations de chasse en plein
air</a> et adoptée par une marge de 69&nbsp;% contre 31&nbsp;%. Avec l’octroi
de la nouvelle autorité au NRC, la loi exige que le CRN, “dans la mesure du
possible… utilise les principes de bonne gestion scientifique dans les
décisions concernant la chasse du gibier.</p>
<section>
  <p>En 2012, le sénateur de l’État Tom Casperson (R-Escanaba), représentant
  un district congestionnel de la Péninsule Supérieure, présenta un projet
  qui répertorierait le loup comme une espèce de gibier. Le projet de loi
  avait été adopté le Parlement avec un appui écrasant, et le gouverneur
  Snyder l’a signé avant la fin de l’année, c’est la <em>loi 520 sur les
  actions publiques (AP)</em>. Une fois désigné comme une espèce de gibier
  par la législature, la responsabilité incombe au CRN de déterminer si une
  saison de chasse est-elle appropriée.</p>
  <p>Après que le projet de loi Casperson soit entré en vigueur, un groupe
  qui lutte contre la chasse dans le Michigan appelé <em>les gardiens
  protecteurs des loups du Michigan</em> (GPLM ou KMWP en anglais) lança une
  pétition pour faire désigner la loi <em>520 AP</em> par un vote public.
  L’initiative avait été soutenue par plus de 255 000 votes, suffisant pour
  forcer le passage de la <em>520 AP</em> par bulletin de vote. En guise de
  réponse, le sénateur Casperson a introduit la loi <em>288 Sénat Bill</em>,
  qui effectivement a court-circuité (est passé au dessus de…) le processus
  de l’initiative de bulletin de vote, faisant taire ainsi les efforts du
  groupes luttant contre la chasse. Ce projet de loi fut également adopté par
  le Parlement avec un soutien écrasant.</p>
</section>
<p>Selon les prescriptions de la loi <em>377 AP</em>, pour prendre des
décisions, on se base sur des principes scientifiques. Alors le CRN a demandé
au MRN du Michigan, de faire une recommandation sur l’opportunité de créer
une saison de chasse. <a data-edgenote="hunt-as-management-tool">Le MRN
a conclu qu’une chasse publique au loup dans trois régions spécifiques de la
Péninsule Supérieure</a>, réduirait les conflits entre les hommes et les
loups sans entraver l’augmentation de la population de loups. La <a
data-edgenote="dnr-management-proposal">proposition de l’unité de gestion
des loups par le MRN a</a> inclue les zones recommandées pour une chasse
limitée, la taille des zones, le nombre de loups retirés par le département
des Services de l’agriculture et de la faune aux États-Unis en 2012, le
nombre de loups autorisé d’être chassé dans chaque région.</p>
<h2>Les pays voisins: des exemples pour le Michigan</h2>
<p>L’Etat du Michigan peut regarder chez ses voisins de l’ouest comment une
chasse au loup pourrait s’y dérouler. Après la radiation de la liste fédérale
au début de 2012, le Minnesota et le Wisconsin, et deux autres États dans
l’ouest des Grands Lacs, où l’on y trouve un segment de population distinct
des États occidentaux, ont mis en place des saisons de chasse cette même
année. Similaires au Michigan, les deux Etats expérimentés ont connu
significativement des troubles politiques importants pendant le processus
décisionnel.</p>
<p>La Chasse au loup du Minnesota a été autorisée par sa législature après un
certain classement des loups comme petits gibiers grâce à la loi de l’État.
Le MRN du Minnesota avait effectué une enquête sur la population globale de
loup en 2008. Cette population compte 2 900 loups. Un arrachage ciblé a été
fixée à 400. Comme dans le Michigan, la chasse au loup du Minnesota a été
scindée en trois territoires — le Centre Est, le Nord-Est et le Nord-Ouest,
avec des objectifs de récolte s’élevant respectivement à 18, 117 et 265. En
2012, la chasse a entraîné 413 loups écoulés par rapport à la cible de
400.</p>
<p>La chasse au loup du Wisconsin a été également autorisée par une loi de
l’État qui admet une chasse au loup et une saison de piégeage appropriées. La
loi 169, signée en avril 2012, autorisa le MRN du Wisconsin de concevoir une
chasse au loup et une saison de piégeage. Le plan de gestion pour les loups
du Wisconsin permet une chasse publique si seulement si la population de
loups est supérieure à 350, <a data-edgenote="hunt-on-native-land-mpr">en dehors des réserves
amérindiennes</a>. L’objectif de population a été atteint en 2004, et le
statut d’État a changé de “menacer” à “protéger les animaux sauvages”. Pour
la chasse de 2012, le MRN avait fixé un quota de 201 loups et avec une
récolte réelle de 117.</p>

<h1>Le choix à portée de main</h1>
<section>
  <p>J. R. Richardson fixe de regard la pile des observations du public, la
  recommandation de DNR et un tas d’interviews d’experts, sachant que le sort
  de la chasse publique au loup dans le Michigan repose sur sa décision. Son
  mandat consiste à fonder sa décision sur des bases scientifiques, et il est
  entrain de regarder le disque dans lequel il est déclaré: “Tout ce que nous
  pouvons faire est fondée sur la science. À la fin de la journée, il y a
  beaucoup de passion qui vont entrainer beaucoup d’émotion. Ainsi nous
  sommes chargés de regarder à travers les émotions et la passion”.</p>
  <p>Mais la science seule ne semble pas avoir obtenu la réponse à la
  question de savoir si une chasse publique devrait se tenir. Par ailleurs,
  le loup gris est comme un animal polarisant et la politique de la question
  ainsi établi, Richardson n’a aucun autre choix que de considérer les
  <a data-edgenote="wolves-in-ojibwe">implications sociales et
  politiques complexes</a> de cette décision.</p>
</section>
<p>Richardson s’est aussi rendu compte que le choix du CRN n’est pas limité à
un simple oui ou non sur une chasse, mais il s’agit de répondre à une série
de questions connexes comme qui devrait faire la chasse et comment cela
devrait être fait. Devrait-on permettre à la fois la chasse et le piégeage,
par exemple&nbsp;? Certains de ses cadres de l’Agence, par exemple, ont fait
valoir que l’Agence devrait s’occuper de la chasse pour la garder sous
contrôle aussi étroitement que possible; d’autres pensaient qu’il était
important de commencer par des moyens de contrôle non létal. Mais d’autres
également ont vu ces options comme un gaspillage de bonnes opportunités
économiques et de loisir.</p>
<section>
  <p>Il se demande donc s’il existe une réponse scientifiquement correcte ou
  une réponse correcte sur le plan éthique. Est-ce que la politique de cette
  affaire peut-elle justifier le fait de décider dans un sens ou dans un
  autre&nbsp;? Par exemple, ses collègues commissaires au CRN
  soutiendront-ils une décision contre la chasse, surtout qu’ils avaient été
  nommés par le gouverneur, quelles seront les réactions de l’Assemblée
  législative, des groupes traditionnels de la commission et du MRN?</p>
  <p>Et quelles sont les implications ou les conséquences sociales plus
  larges de la décision? Si les chasseurs considèrent les loups comme du
  gibier par opposition à la concurrence avec les cerfs ou autres gibiers,
  cela allégera-t-il quelques ferveurs ou dévotions des défenseurs du loup,
  et cela donnera-t-il au MRN plus de latitude pour prendre des décisions de
  gestion scientifiquement établies à l’avenir? Est-ce que c’est le devoir du
  CRN de regarder aux valeurs culturelles et aux préoccupations de
  souveraineté des tribus amérindiennes du Michigan&nbsp;? Les GPLM et la
  HSUS ont suscité l’indignation du public autour de la chasse, mais le CRN
  doit accorder la priorité à l’intérêt supérieur de tous les citoyens du
  Michigan. Quelle importance revêt l’opinion publique, étant donné que la
  grande majorité des habitants du Michigan vivent hors de la Péninsule
  Supérieure et n’ont jamais rencontré un loup?</p>
</section>
