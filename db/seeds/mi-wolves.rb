<h1>Introduction</h1>
<p><strong>J. R. Richardson</strong> took a break from some documents he was
reading to think about a difficult decision that he would face in the
upcoming days: whether to vote in favor of allowing a wolf hunt to take place
in the state of Michigan. As chair of the <em>Natural Resources
Commission</em> (NRC), a seven-person policy advisory body to the state’s
<em>Department of Natural Resources</em> (DNR), Richardson had dealt with his
fair share of contentious issues during his tenure at the NRC since 2007. But
he could remember none that had aroused such passion from all sides as the
wolf hunt issue. As a result, he felt a great deal of pressure to lead the
NRC to make the right decision based on sound science, respect <a
data-edgenote="a-heated-debate">for the needs of the people who are
affected</a> by the presence of wolves, and sensitivity to public
opinion.</p>
<p>The NRC, whose job is to regulate game species, did not appear to be the
most likely decision making body when it came to wolves in Michigan. Indeed,
given the systematic extermination of the species throughout the United
States during the 19th and early 20th centuries and its subsequent listing as
an endangered species in 1974, the idea of wolf hunts was unimaginable for
decades. However, the miraculous comeback of the wolf led to its de-listing
in January 2012, a move that devolved control back to the states, including
Michigan, where a growing wolf population now resides. In December 2012,
Michigan Governor Rick Snyder and the Michigan legislature officially
designated the wolf as a game species, which landed responsibility for
regulating any public wolf hunt on the plate of the NRC.</p>
<section>
  <p>As a lifelong resident of Michigan’s Upper Peninsula (U.P.), where the
  state’s wolf population resides, as well as an avid outdoorsman,
  businessman, and engineer with extensive experience in natural resource
  issues, Richardson had conflicting personal and professional feelings about
  the wolf harvest. On the one hand, he saw the wolf’s comeback as a sign that
  the natural environment was healthy and thriving, and he wanted to
  protect that ecological balance. On the other, he personally knew <a
  data-edgenote="residents-weigh-hunt">some whose livestock had been
  preyed on by wolves</a> and had firsthand knowledge of the human–wolf
  conflicts that made this issue so politically charged. The science,
  politics, and community concerns surrounding the possibility of a wolf hunt
  meant that whatever decision the NRC made, not everyone would be happy. And
  Richardson would be the point person for the NRC’s decision and would have
  to take any heat from the governor and legislature, wolf advocates, tribal
  groups, hunters, ranchers, and the public, who would be scrutinizing this
  decision heavily.</p>
  <p>Now, on the eve of this momentous decision, Richardson reflected on his
  options. Should he heed the calls of anti-hunt advocates and some
  scientists that wolves should continue to be fully protected? Should he
  allow the public hunt to proceed, or encourage the DNR to find other ways
  to control the population? How might conflicts between wolves and humans be
  managed most effectively? What are the ramifications of the different
  decisions for the stakeholders involved? Richardson returned to his reading
  with these questions weighing heavily on his mind.</p>
</section>

<h1>The history of gray wolves in the United States and Michigan</h1>
<section>
  <p>The history of <a data-edgenote="gray-wolf">gray
  wolves</a> in Michigan, which parallels the greater story of the species in
  the Great Lakes and throughout North America, is woven with trials and
  triumphs. Native to Michigan, wolves once roamed every county in the state,
  coexisting with humans.</p>
  <p>In the 1800s, westward settlement of the United States helped initiate
  the human–wolf conflicts that were to plague the gray wolf. Animals that
  had once been prey for wolves—bison, elk, and moose—were killed by new
  settlers for food or for sport. As their natural food source declined,
  wolves turned to settlers’ sheep and cattle to feed themselves and
  unintentionally sparked an extermination campaign that was to last over a
  hundred years. Ranchers and professional “wolfers” indiscriminately killed
  the animals to protect their herds, using poison, traps, and other methods.
  The extermination escalated as the federal government promulgated a
  nationwide eradication program, offering bounties for skins of the dead
  animals. Even President Theodore Roosevelt, otherwise known as a champion
  for environmental conservation, reflected the feeling of a nation when he
  termed wolves “beasts of waste and desolation.”</p>
</section>
<section>
  <p>The federal wolf pelt bounty program decimated wolf populations across
  the country, and Michigan’s wolves were not spared. Wolves were eliminated
  from the southern part of the state by the 1830s, and by the 1900s they had
  all but disappeared. A tiny population remained in the Isle Royale island
  north of <a data-edgenote="michigan">Michigan’s Upper
  Peninsula</a>, considered possibly one of the last wolf populations in
  North America during <a data-edgenote="gray-wolf-range">the
  final stage of wolf extermination in the 1960s</a>.</p>
  <p>In a major turnaround for the species, Michigan followed other states in
  legislating protection for wolves in 1965. This gave the few remaining
  individuals—estimated to be six in the Upper Peninsula and an additional
  tiny population on Isle Royale—reprieve from systematic hunting, allowing
  them to begin a slow recovery. Their recovery was bolstered by the 1974
  listing of wolves as an endangered species soon after the passage of the
  1973 federal <em>Endangered Species Act</em> (ESA).</p>
</section>
<p>There were signs that wolves were staging a comeback, as reports of
sightings in Michigan’s U.P. began to increase in the 1970s. To try to speed
up this recovery, biologists made an attempt in 1974 to introduce a pack of
four from Minnesota into the U.P. The plan failed when humans killed all four
wolves before they had time to reproduce, causing Michigan’s DNR to decide
that it would no longer try to intervene in the reestablishment of wolves in
the state. This natural recovery gained steam when a pair of wolves was
discovered to have immigrated from nearby states into the central U.P. in the
1980s. As the pair began to reproduce in the early 1990s, <a
data-edgenote="mi-wolf-population">a steady increase in the population
ensued</a>. In 2010, 687 wolves were documented to be living in the state.
Simultaneously, the public was becoming increasingly supportive of the wolf
comeback, with a majority of Michigan residents supporting wolf recovery in
the state.</p>
<p>Federal and state protection was pivotal in wolves’ recovery. The Wolf
Recovery Plan, developed to meet ESA requirements, stipulated that the
Wisconsin–Michigan population must exceed 100 for a minimum of five
consecutive years to ensure the survival of the species, which was achieved
by 1994. In 1997, <a data-edgenote="perversion-of-democracy">Michigan
completed its own recovery plan</a>, the Michigan Wolf Recovery and
Management Plan, recommending a population of 200 for five consecutive years.
Meeting these goals would pave the way for the removal of the species from
state and federal endangered species lists, and indeed the Michigan
Legislature and the U.S. Fish and Wildlife Service removed Michigan’s wolves
from the state and federal endangered species lists in 2009 and 2012,
respectively. Removal from the federal list gave full control to the state of
Michigan to manage its wolf populations.</p>

<h1>Biology, conservation, and scientific management</h1>
<h2>Importance of wolves in the ecosystem</h2>
<section>
  <p>Gray wolves are considered to be a <strong>keystone</strong> species
  that plays a unique role in maintaining the health of many ecosystems.
  Biological study of Yellowstone National Park and other areas that have
  been repopulated by wolves sheds light on the cascade of impacts that their
  removal and return have had on the ecosystem. To begin, wolves help
  regulate the size and health of prey populations by selecting young or
  infirm animals to attack. This simultaneously helps manage the number of
  prey that reaches reproductive maturity and removes diseased animals from
  the herd, stemming population growth and reducing disease transmission.</p>
  <p>Furthermore, the presence of wolves in the landscape alters the browsing
  patterns of prey to the benefit of vegetation. Deer, bison, elk, and other
  browsing species tend to feed in the open on saplings and new growth when
  predators are absent, which reduces the amount of vegetation that grows in
  an area. The presence of wolves compels these animals to seek cover and
  browse in less sensitive areas. This allows for vegetation to grow and for
  riparian areas to thrive. The cascade effect continues as heavier
  vegetation attracts beavers, small rodents, birds, and other species whose
  presence creates habitat for fish and improves the health of waterways.
  Finally, carcasses left by wolves provide food for scavengers, contributing
  to the biodiversity of the landscape.</p>
</section>
<p>Although wolf reintroduction has led to a dramatic restoration of
biodiversity and riparian ecosystems in Yellowstone, researchers are
realizing that <a data-edgenote="death-of-832f">reintroducing
a top predator after a 70-year absence</a> does not solve all problems. In
some areas, willows and cottonwoods are still failing to thrive and beaver
have not returned. This is because heavy browsing in the absence of wolves
actually changed the shape and flow rate of rivers. The habitat became
unsuitable for willows and beaver, while other areas became more accessible
to bison, an herbivore unaffected by wolves. Even though introduced wolves
have now reduced browsing by elk, it will take more to reverse the changed
hydrology and landscape of Yellowstone.</p>
<p>In Michigan, wolves are a key predator of white-tailed deer, a browsing
species commonly found throughout the United States. In part because of a
dearth of natural predators, deer densities are dramatically higher today
than historical estimates, and over-abundant deer can have devastating
impacts on native plant diversity and forest ecology. As Forest Service
researcher Stephen Horsley notes: “Foraging deer ‘vacuum up’ the seedlings of
highly preferred species, reducing plant diversity and in the extreme,
creating near mono-cultures.”</p>
<section>
  <p>Hunters have noted that the growing wolf population in the Upper
  Peninsula of Michigan coincides with <a
  data-edgenote="bear-wolf-coyote-deer-pop">a declining deer population</a>.
  However, recent studies of causes of deer mortality in this area point
  instead to hard winters and coyote as the main drivers.</p>
  <p>Maintaining the ecological benefit of wolves is an explicit goal of the
  <em>Michigan DNR Wolf Management Plan</em>, which recognizes that wolves
  can control and improve the health of prey, and have positive indirect
  effects on plant communities. Although 200 wolves is the stated minimum
  viable population of wolves, it is recognized that this size may not be
  sufficient to provide these ecological benefits. Robert Beschta, who
  studied the Yellowstone system for over a decade, states that for
  ecological recovery, “It may be necessary for wolves not only to be present
  but to have an ecologically effective density.”</p>
</section>
<h2>Responsibility for wolf management in Michigan: The DNR and other
entities</h2>
<section>
  <p>The Michigan Department of Natural Resources is responsible for
  conserving, protecting, and managing the state’s natural and cultural
  resources. The DNR aims to balance thriving ecosystems, healthy natural
  resource-based economies, and sustainable recreation and enjoyment of those
  resources.&nbsp;</p>
  <p>Once wolves were delisted from the federal endangered species list in
  2012, wolf management was added to the list of DNR’s responsibilities.
  Because it regulates both wildlife and game species, the DNR approaches
  wolf management both from the perspective of wolves as a wildlife species
  and as a game animal. As a result, decision-making about how to manage
  Michigan’s wolf population takes into account biological, social, and
  economic considerations, as discussed further below.</p>
</section>
<p>Various committees and groups within the DNR contribute to wildlife
management decision making in the state. The Michigan <em>Wolf Management
Advisory Council</em> (WMAC) was formed to review the Wolf Management Plan
periodically and to provide guidance to the DNR on what changes should be
made. WMAC is represented by people from relevant agencies and stakeholder
groups. At a higher level, the NRC, a group of seven governor-appointed
members, is responsible for helping to develop policy that influences DNR
direction and priorities. The NRC also has authority to regulate game
species.</p>
<h2>Wolf Management</h2>
<p>State and federal regulatory bodies consider how to achieve both social
and ecological goals in wolf management decision-making. Whereas the 1997
Michigan <em>Wolf Recovery and Management Plan</em> was created largely in
the context of protecting the species and ensuring its successful recovery,
the 2008 version of the plan was updated to take <a
data-edgenote="recovery-management-plan-goals">a more holistic view of
protecting
wolves</a> while addressing their coexistence with humans.</p>
<p>Defining successful wolf management depends on the goals of managing the
population and the context for establishing those goals. The DNR notes that
the <strong>biological carrying capacity&nbsp;</strong>and <strong><a
data-edgenote="measure-tolerance-carrying-capacity">social carrying
capacity</a></strong>&nbsp;of wolves should be considered. Biological
carrying capacity refers to the concept that the population of a species is
limited by the ability of the environment to support it with food, water, and
habitat. Social carrying capacity refers to how human tolerance for a species
might limit its population. Successful management of wolves, then, means
balancing the biological viability of the species and its important role in
the ecosystem with the tolerance of the humans who live in its presence. As
wildlife biologist David Hammill put it in testifying before the NRC, the
viability of the wolf population in the state depends on the ability of
humans and wolves to coexist peacefully. As a result, where species are in
particular conflict with humans, conflict management is critical to sound
scientific decision-making. Management goals are developed to balance
ecosystem benefits and human conflicts.</p>
<section>
  <p>The <em>Michigan Wolf Roundtable</em>, a stakeholder committee
  reflecting various points of view, was convened by the DNR to provide
  recommendations on wolf issues. The committee’s 2006 report indicated that
  “goals for wolf management should be based on wolf impacts rather than wolf
  abundance or numbers” that balance ecological and social concerns. The
  20-member committee contained representatives from groups as divergent as
  the <em>Michigan Farm Bureau</em>, the <em>Upper Peninsula Sportsmen’s
  Alliance</em>, the <em>Michigan Humane Society</em>, and the <em>Timber
  Wolf Alliance</em>. Members reached consensus on the need to have lethal
  control of wolves as an “option for response to conflicts involving wolves
  and livestock. However, the revised wolf plan should place a high priority
  on developing, evaluating and applying non-lethal management methods.”</p>
  <p>While the group could agree on many things, they could not agree on
  whether a hunting/trapping season was appropriate. “After substantial
  deliberation, we concluded consensus on any guiding principles regarding
  this issue was not possible because the disagreement focused primarily on
  important differences in fundamental values.”</p>
</section>
<p>Managing the wolf population can involve various methods of population
control, including fertility control, translocation to less populated areas,
and selective killing. “Passive management,” which essentially allows the
population to grow to its biological carrying capacity, is also an option,
though it does not address the issue of social carrying capacity. Generally,
today these methods are carried out by professionals such as those in the
U.S. Fish and Wildlife Service, though in the past the government has allowed
and even paid individuals to kill wolves. The designation of wolves as a game
species that can be hunted recreationally creates another option for managing
the wolf population with potential benefits. The level of take is determined
by scientists and managers to ensure viability. Hunters benefit from a new
opportunity, and residents perceive that the human–wolf conflict issue is
being addressed.</p>
<p>Scientists on both sides of the debate appear to recognize the importance
of balancing social and ecological concerns, but disagreement arises over how
those concerns should be weighed against one another. Some wolf advocate
groups, whose members include both scientists and onscientists, question this
approach to scientific management, asserting that there is no biological need
or solid scientific support for a public wolf hunt. Moreover, they argue that
public concerns about human–wolf interactions are overblown and that those
interactions tend to be so rare that a public wolf hunt is not a sensible
approach for dealing with them.</p>

<h1>Humans and wolves: A delicate relationship</h1>
<p>Wolves hold a distinct place in Western European culture. Children’s
stories and fairy tales written centuries ago provide a glimpse of one
attitude towards the animal: <em>The Boy Who Cried Wolf</em>, <em>Little Red
Riding Hood</em>, and <em>The Three Little Pigs</em> all feature the
archetypal big bad wolf. But in the 21st century with an urban population, a
more developed landscape, and vastly smaller numbers of predators, is the
potential for attack by wolves a real problem? Do they need to be treated as
a threat to life and livelihood?</p>
<h2>Attacks on people and dogs</h2>
<p>There are few documented wolf-related human deaths in North America. Since
2000, three documented deaths have occurred as a result of wolf attacks (two
in Alaska and one in Ontario). A 2002 report produced by the <em>Norwegian
Institute for Nature Research</em> cited 18 total wolf attacks in North
America in the 20th century. The report identifies four factors associated
with attacks on humans: rabies, habituation (when wolves lose the fear of
humans), provocation, and highly modified environments. Also in 2002, the
<em>Alaska Department of Fish and Game</em> stated that no human deaths have
been attributed to healthy wolves since 1900, with the reported deaths being
attributed to wolves afflicted with rabies.</p>
<p>While no documented wolf attacks on humans have occurred in Michigan,
there have been a handful of attacks on dogs. A 2011 paper found 34 instances
of domestic dog depredations between 1996 and 2008. Moreover, a majority of
<a data-edgenote="too-close-for-comfort">residents in the U.P.
believe that wolves are becoming increasingly comfortable with humans</a>,
which increases the risks of wolves attacking people.</p>
<section>
  <p>In an April 2013 memo, the DNR comments on human–wolf interactions in
  Gogebic County, MI:</p>
  <blockquote>
    <p>Since 2010, we have recorded 91 complaints of nuisance wolf behavior
    including wolves traveling within the city limits, wolves chasing dogs in
    residential areas, and wolves traveling in close proximity to children
    waiting for school buses or at day care facilities. USDA Wildlife
    Services, under our direction, has spent a considerable amount of time
    responding to these complaints. Since 2010, they have employed harassment
    techniques (cracker shells) and provided technical advice to residents on
    avoiding negative wolf–human interactions. When the nonlethal techniques
    failed to resolve the problems, Wildlife Services removed 17 wolves from
    the area.</p>
  </blockquote>
  <p>Data from the last century indicate that wolf attacks on people and pets
  in North America are rare, but the past may not be indicative of the
  future, especially when taking into account the absence of wolves for most
  of the 20th century.</p>
</section>
<h2>Livestock disturbances</h2>
<p>The most visible type of livestock disturbance is depredation, when wolves
attack and injure or kill livestock. Across North America, sheep and cattle
are the most common targets, but any type of livestock can be subject to
depredation. Between 1996 and 2008, 87 livestock depredations were recorded
in Michigan, <a data-edgenote="most-polarizing-farmer">with 70% of
events involving cattle</a>. There is a positive relationship between wolf
abundance and cattle depredation in the U.P., and the 1996–2008 data suggest
that for each additional 100 wolves, there will be <a
data-edgenote="cattle-depredations-mi-radio">an additional three cattle
depredations</a>. Even when wolf incursions onto farm or ranch land do not
directly result in injury, close encounters can frighten cattle and cause
them to be less willing to graze. Reduced grazing results in smaller and less
valuable cattle. Wolf encounters can also hamper the ability of dogs in
herding cattle, negatively affecting the efficiency of livestock operations.
Farmers and ranchers note that indirect impacts on productivity do not
qualify for compensation under programs designed to mitigate the impact of
wolf depredations.</p>

<h1>A political and regulatory saga</h1>
<p>The path leading up to the NRC’s decision is marked by political drama, in
which the Michigan Legislature repeatedly took action to advance the state
toward a wolf hunt, hunting groups tried to use ballot measures to promote a
hunt, and anti-hunting groups sought to use them to stop the hunt. <em>Public
Act 377</em> granted the NRC exclusive authority to regulate the taking of
game in Michigan, removing that power from the Director of the <em>Department
of Natural Resources</em>. The proposal was an outgrowth of a ballot
initiative funded by <a data-edgenote="hunting-conservation-economics">outdoor
recreation and hunting organizations</a>, &nbsp;and passed by a 69% to 31%
margin. Along with granting the NRC new authority, the statute requires that
the NRC, “to the greatest extent practicable… use principles of sound
scientific management in making decisions regarding the taking of game.”</p>
<section>
  <p>In 2012, State Senator Tom Casperson (R-Escanaba), representing a U.P.
  congressional district, introduced a bill that would list the wolf as a
  game species. The bill passed the legislature with overwhelming support,
  and Governor Snyder signed it before the end of the year, creating
  <em>Public Act 520</em>. Once designated as a game species by the
  legislature, the responsibility falls to the NRC to determine whether a
  hunting season is appropriate.</p>
  <p>After the Casperson bill went into effect, an anti-hunting group in
  Michigan called <em>Keeping Michigan Wolves Protected</em> (KMWP) launched
  a petition effort to refer <em>PA 520</em> to a public vote. The initiative
  submitted more than 255,000 votes, sufficient to force <em>PA 520</em> onto
  the ballot. In response, Senator Casperson introduced <em>Senate Bill
  288</em>, which effectively bypassed the ballot initiative process, making
  the anti-hunting groups’ efforts moot. The bill passed the legislature with
  overwhelming support.</p>
</section>
<p>In accordance with the requirements of <em>PA 377</em> to make decisions
based on sound science, the NRC asked the Michigan DNR to make a
recommendation on whether or not to create a hunting season. <a data-edgenote=
"hunt-as-management-tool">The DNR concluded that a public wolf
hunt in three specific areas of the U.P</a>. would reduce human–wolf
conflicts without hampering the overall abundance of the wolf population. The
<a data-edgenote="dnr-management-proposal">DNR Wolf Management Unit
proposal</a> included recommended areas for a limited hunt, the size of the
areas, the number of wolves removed by the United States Department of
Agriculture Wildlife Services in 2012, and the number of wolves authorized
for harvesting in each area.</p>
<h2>Neighboring states: Examples for Michigan?</h2>
<p>Michigan can look to its neighbors to the west for a picture of how a wolf
hunt might play out. Following federal delisting early in 2012, Minnesota and
Wisconsin, the two other states in the tri-state Western Great Lakes Distinct
Population Segment, put into place hunting seasons later that same year.
Similar to Michigan, both states experienced significant political turmoil
during the decision-making process.</p>
<p>Minnesota’s wolf hunt was authorized by its legislature after classifying
wolves as small game through state statute. The Minnesota DNR performed a
comprehensive wolf population survey in 2008 and counted 2,900 wolves. A
target harvest was set at 400. Like Michigan, the Minnesota wolf hunt was
split into three territories—east central, northeast, and northwest—with
harvest targets of 18, 117, and 265, respectively. The 2012 hunt resulted in
413 wolves taken compared to the target of 400.</p>
<p>Wisconsin’s wolf hunt was also authorized through a state statute that
requires a wolf hunting and trapping season. Act 169, signed in April 2012,
authorized the Wisconsin DNR to design an appropriate hunting and trapping
season. Wisconsin’s wolf management plan allows for a public hunt if the wolf
population&nbsp;exceeds 350,&nbsp;<a
data-edgenote="hunt-on-native-land-mpr">outside of Native American
reservations</a>. The population goal was met in 2004, and the state status
was changed from “threatened” to “protected wild animal.” For the 2012 hunt,
the DNR set a quota of 201, with an actual harvest of 117.&nbsp;</p>

<h1>The choice at hand</h1>
<section>
  <p>J. R. Richardson stares at the stack of public comments, the DNR
  recommendation, and a pile of expert interviews, knowing that the fate of
  public wolf hunting in Michigan rests on his decision. His mandate is to
  base his decision on sound science, and he is on record stating, “All we
  can do is based on the science. At the end of the day, there is a lot of
  passion that is going to bring out a lot of emotion. What we are charged to
  look through is the emotion and passion.”</p>
  <p>But science alone does not seem to hold the answer to whether a public
  hunt should be held. Besides, the gray wolf is such a polarizing animal and
  the politics of the issue so charged, Richardson has no choice but to
  consider the <a data-edgenote="wolves-in-ojibwe">complex
  social and political implications</a> of the decision.</p>
</section>
<p>Richardson has also become aware that the NRC’s choice is not limited to a
simple yes or no on a hunt, but a range of related questions as to who should
do the hunting and how it should be done. Should both hunting and trapping be
allowed, for example? Some of his agency managers, for example, have argued
that the agency should carry out the hunt to keep it as tightly controlled as
possible; others thought that it was important to start with nonlethal means
of control. Others viewed these options as a waste of a good recreational and
economic opportunity.</p>
<section>
  <p>He wonders whether there is a scientifically correct answer? Or an
  ethically correct answer? Do the politics of the matter warrant a decision
  in one direction or another? For example, will his fellow commissioners on
  the NRC support a decision against the hunt, given their appointments by
  the governor, the reaction by the legislature, and the traditional
  constituencies of the commission and DNR?</p>
  <p>And what are the broader social implications of the decision? If hunters
  view wolves as a game animal as opposed to competition for deer or other
  game, will this alleviate some of their antiwolf fervor and give the DNR
  more latitude to make scientifically based management decisions in the
  future? What is the duty of the NRC regarding the cultural values and
  sovereignty concerns of Michigan’s Native American tribes? KMWP and the
  HSUS have sparked public outrage around the hunt, but the NRC must
  prioritize the best interests of all citizens of Michigan. How important is
  public opinion, given that the vast majority of Michiganders live outside
  of the U.P. and will never encounter a wolf?</p>
</section>
NARRATIVE
